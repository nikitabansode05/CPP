//SWITCH CASE STATEMENT
#include<iostream>
using namespace std;
int main(){
    int age;
    switch(age)
    {
        case 18:
        cout<<"You are 18"<<endl;
        case 22:
        cout<<"You are 22"<<endl;
        case 2:
        cout<<"You are 2"<<endl;
        break;
        default:
        cout<<"no special cases"<<endl;
        break;
    }
    return 0;
}


